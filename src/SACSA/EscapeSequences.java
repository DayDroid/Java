package SACSA;

public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("test"); //regular message that prints "test"
        System.out.println("\t test"); //adds a tab sequence to test
        System.out.println("\"test\""); // adds quotes to test = "test" "\" \"
        System.out.println("test");
    }
}