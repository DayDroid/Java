package SACSA;

public class Conditionals {
    public static void main(String[] args) {

        /*
         *
         * if() { statements perform a block of code if conditions are met
         * */

        int x = 75;

        if (x >= 75) {
            System.out.println(">= 76");
        } else if (x >= 16) {
            System.out.println("x <= 18");
        } else {
            System.out.println("false");
        }
    }

}